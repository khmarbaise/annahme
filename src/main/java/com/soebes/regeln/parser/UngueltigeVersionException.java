package com.soebes.regeln.parser;

public class UngueltigeVersionException extends Exception {
    /**
     * 
     */
    private static final long serialVersionUID = -2838443398047832060L;


    public UngueltigeVersionException() {
        super();
    }

    public UngueltigeVersionException(String message, Throwable cause) {
        super(message, cause);
    }

    public UngueltigeVersionException(String message) {
        super(message);
    }

    public UngueltigeVersionException(Throwable cause) {
        super(cause);
    }

}
